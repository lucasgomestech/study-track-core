/**
 * Enum for person gender.
 *
 * @readonly
 * @enum {string}
 */
export enum Gender {
    /** Person identified as male. */
    MALE = 'MALE',

    /** Person identified as female. */
    FEMALE = 'FEMALE',

    /** Person identified as other. */
    OTHER = 'OTHER',

    /** Person who does not want to identify. */
    ANONYMOUS = 'ANONYMOUS',
}
